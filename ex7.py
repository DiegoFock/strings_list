#Crie uma string contendo uma frase e conte quantas vezes uma determinada palavra
#a. Exemplo: na frase "Três pratos de trigo para três tigres tristes.", “três” = 2

frase = "O Flamengo não é time, o Flamengo é seleção"
palavra = "Flamengo"
ocorrencias = frase.count(palavra)
print("A palavra", palavra, "aparece", ocorrencias, "vezes na frase.")

